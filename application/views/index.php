<script>
  // Set the event date (29 October 2025)
  const eventDate = new Date("October 28, 2025 00:00:00").getTime();

  const updateCountdown = () => {
    const now = new Date().getTime();
    const timeLeft = eventDate - now;

    if (timeLeft < 0) {
      document.getElementById("days").innerText = 0;
      document.getElementById("hours").innerText = 0;
      document.getElementById("minutes").innerText = 0;
      document.getElementById("seconds").innerText = 0;
      return;
    }

    const days = Math.floor(timeLeft / (1000 * 60 * 60 * 24));
    const hours = Math.floor((timeLeft % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    const minutes = Math.floor((timeLeft % (1000 * 60 * 60)) / (1000 * 60));
    const seconds = Math.floor((timeLeft % (1000 * 60)) / 1000);

    document.getElementById("days").innerText = days;
    document.getElementById("hours").innerText = hours;
    document.getElementById("minutes").innerText = minutes;
    document.getElementById("seconds").innerText = seconds;
  };

  // Initial call and then update every second
  updateCountdown();
  setInterval(updateCountdown, 1000);
</script>

<!-- Counter Script (Slow Smooth Animation) -->
<script>
  // Function to animate numbers slowly
  function animateCounter(counter) {
    const target = +counter.getAttribute('data-target');
    let count = 0;
    const duration = 2000; // total animation time in ms
    const stepTime = 20; // update interval in ms
    const steps = duration / stepTime;
    const increment = target / steps;

    const updateCount = () => {
      count += increment;
      if (count < target) {
        counter.innerText = Math.floor(count) + "+";
        setTimeout(updateCount, stepTime);
      } else {
        counter.innerText = target + "+";
      }
    };
    updateCount();
  }

  // Trigger when section is visible
  const counters = document.querySelectorAll('.counter');
  let started = false;

  window.addEventListener('scroll', () => {
    const statsSection = document.querySelector('#stats');
    if (!statsSection) return;
    const sectionPos = statsSection.getBoundingClientRect().top;
    const screenPos = window.innerHeight / 1.2;

    if (!started && sectionPos < screenPos) {
      counters.forEach(counter => animateCounter(counter));
      started = true; // Run only once
    }
  });
</script>

